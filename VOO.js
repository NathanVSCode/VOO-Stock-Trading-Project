  fn_CalculateVOO = function(){
    let vooMonth =  document.getElementById('vooMonth').value;
    let vooYear = document.getElementById('vooYear').value;
    let dollarAmount = document.getElementById('dollarAmount').value;
  
    if(vooMonth>12 || vooMonth<1){
     let displayResult = document.getElementById('displayResult')
     displayResult.innerText = `Error: Choose a month between Jan and Dec`;
     displayResult.style.color = 'red';
    }
    else if (vooYear>2023 || vooYear<2002) {
      let displayResult = document.getElementById('displayResult')
      displayResult.innerText = `Error: Choose a year between 2002 and 2023`
      displayResult.style.color = 'red';
    }
    else if (dollarAmount<=0) {
      let displayResult = document.getElementById('displayResult')
      displayResult.innerText = `Error: Make your dollar amount greater than 0`
      displayResult.style.color = 'red';
    }
      else {
      let vooYearMontharray = smp500Data.findIndex(item => item[0] == vooMonth && item[1]==vooYear);
      let vooPrice = smp500Data[vooYearMontharray][2];
      let shareAmount = (dollarAmount/vooPrice)*440;
      let roundShareAmount = Math.round(shareAmount)
      let displayResult = document.getElementById('displayResult');
      displayResult.innerText = `$${dollarAmount} invested back then is worth about $${roundShareAmount} in 2024. That is a bunch of green cash!`;
      displayResult.style.color = 'green';
    }
    };